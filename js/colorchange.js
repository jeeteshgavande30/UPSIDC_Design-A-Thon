var count = 0;
function myFunction() {
  if (count%2 == 0) {
    count++;
    var colored = document.getElementById("j-sec31");
    colored.classList.remove("color-change2");
    colored.classList.add("color-change");
  }
  else {
    count++;
    var uncolored = document.getElementById("j-sec31");
    uncolored.classList.remove("color-change");
    uncolored.classList.add("color-change2");
  }
}

